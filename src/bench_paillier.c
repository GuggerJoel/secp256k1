static void bench_paillier_setup(void* arg) {
    int i;
    bench_paillier_data *data = (bench_paillier_data*)arg;

    for (i = 0; i < 32; i++) {
        data->msg[i] = i + 1;
    }

    data->pub = secp256k1_paillier_pubkey_create();
    data->enc = secp256k1_paillier_message_create();
    CHECK(secp256k1_paillier_pubkey_parse(data->pub, raw_pubkey, 1041) == 1);
}

static void bench_paillier_teardown(void* arg) {
    bench_paillier_data *data = (bench_paillier_data*)arg;

    secp256k1_paillier_pubkey_destroy(data->pub);
    secp256k1_paillier_message_destroy(data->enc);
}

static void bench_paillier_enc(void* arg) {
    int i;
    bench_paillier_data *data = (bench_paillier_data*)arg;

    for (i = 0; i < 100; i++) {
        int j;
        size_t out;
        unsigned char* ser;
        CHECK(secp256k1_paillier_encrypt(data->enc, data->msg, 32, data->pub, paillier_nonce_function) == 1);
        ser = secp256k1_paillier_message_serialize(&out, data->enc);
        for (j = 0; j < 32 && j < (int)out; j++) {
            data->msg[j] = ser[j];
        }
    }
}

int main(void) {
    bench_paillier_data data;

    run_benchmark("paillier_enc", bench_paillier_enc, bench_paillier_setup, bench_paillier_teardown, &data, 10, 100);

    return 0;
}
