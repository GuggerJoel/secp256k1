void tracescal(secp256k1_scalar s) {
    unsigned char buf[32] = {0};
    mpz_t num;
    mpz_inits(num, NULL);
    secp256k1_scalar_get_b32(buf, &s);
    mpz_import(num, (size_t)32, 1, sizeof(buf[0]), 1, 0, buf);
    mpz_out_str (stdout, 10, num);
    fprintf(stdout, "\n");
    mpz_clears(num, NULL);
}

void tracechar(unsigned char *d, size_t l) {
    unsigned long i = 0;
    for (; i < l; i++) {
        printf("0x%02hhX, ", d[i]);
        if ((i+1) % 16 == 0)
            printf("\n");
    }
    printf("\n> %lu\n", l);
}

void tracepub(secp256k1_context *cctx, secp256k1_pubkey p) {
    int i = 0;
    size_t size = 65;
    unsigned char b[65] = {0};
    if (secp256k1_ec_pubkey_serialize(cctx, b, &size, &p, SECP256K1_EC_UNCOMPRESSED)) {
        for (i = 0; i < 65; ++i)
            printf("%02hhX", b[i]);
        printf("\n");
    }
}

void traceparam(secp256k1_context *cctx, secp256k1_threshold_signature_params p, int o) {
    printf("%s : ", "k:");
    tracescal(p.k);
    printf("%s : ", "z:");
    tracescal(p.z);
    if (o) {
        printf("%s : ", "r:");
        tracepub(cctx, p.r);
    }
}

static int eczkp_rdn_function(mpz_t res, const mpz_t max, const int flag) {
    int ret = 0;
    int cpt = 0;
    mpz_t gcd;
    mpz_init(gcd);
    do {
        ret = secp256k1_paillier_nonce_function_default(res, max);
        if (ret && flag == SECP256K1_THRESHOLD_RND_INV) {
            mpz_gcd(gcd, res, max);
            ret = mpz_cmp_ui(gcd, 1) != 0;
        }
        cpt++;
    } while (!ret && cpt <= 10);
    if (cpt == 10) {
        printf("%s\n", "error");
        ret = 0;
    }
    mpz_clear(gcd);
    return ret;
}

